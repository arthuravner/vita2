package model.filtro;

public class FiltroAvaliacao {

	private String descLinkAvaliacao;

	private String turmaNome;

	private Integer turmaAno;

	private Integer turmaPeriodo;

	private Integer turmaId;

	private Integer professorId;

	private Integer linkId;

	public String getDescLinkAvaliacao() {
		return descLinkAvaliacao;
	}

	public void setDescLinkAvaliacao(String descLinkAvaliacao) {
		this.descLinkAvaliacao = descLinkAvaliacao;
	}

	public String getTurmaNome() {
		return turmaNome;
	}

	public void setTurmaNome(String turmaNome) {
		this.turmaNome = turmaNome;
	}

	public Integer getTurmaAno() {
		return turmaAno;
	}

	public void setTurmaAno(Integer turmaAno) {
		this.turmaAno = turmaAno;
	}

	public Integer getTurmaPeriodo() {
		return turmaPeriodo;
	}

	public void setTurmaPeriodo(Integer turmaPeriodo) {
		this.turmaPeriodo = turmaPeriodo;
	}

	public Integer getTurmaId() {
		return turmaId;
	}

	public void setTurmaId(Integer turmaId) {
		this.turmaId = turmaId;
	}

	public Integer getProfessorId() {
		return professorId;
	}

	public void setProfessorId(Integer professorId) {
		this.professorId = professorId;
	}

	public Integer getLinkId() {
		return linkId;
	}

	public void setLinkId(Integer linkId) {
		this.linkId = linkId;
	}

}
